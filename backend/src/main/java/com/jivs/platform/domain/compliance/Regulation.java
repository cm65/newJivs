package com.jivs.platform.domain.compliance;

/**
 * Enum representing data protection regulations
 */
public enum Regulation {
    GDPR,           // General Data Protection Regulation (EU)
    CCPA,           // California Consumer Privacy Act (US)
    HIPAA,          // Health Insurance Portability and Accountability Act (US)
    PIPEDA,         // Personal Information Protection and Electronic Documents Act (Canada)
    LGPD,           // Lei Geral de Proteção de Dados (Brazil)
    PDPA_SINGAPORE, // Personal Data Protection Act (Singapore)
    PDPA_THAILAND,  // Personal Data Protection Act (Thailand)
    DPA_UK,         // Data Protection Act (UK)
    APPI            // Act on the Protection of Personal Information (Japan)
}
